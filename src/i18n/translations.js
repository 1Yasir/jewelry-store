export function getTranslation(lang, key) {
  const keys = key.split(".");
  let value = translations[lang] ?? translations.en;

  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      let fallback = translations.en;
      for (const fk of keys) {
        fallback = fallback?.[fk];
      }
      return fallback ?? key;
    }
  }

  return value;
}
